

//below is the checking to see what font to load at start -->

	function carry_font(font){
		if(font == "Anonymous Pro"){
			set_anon_pro();
		}
		if(font == "Ubuntu"){
			set_ubuntu();
		}
		if(font == "Roboto Mono"){
			set_roboto();
		}
		
		if(font == "Courier Prime"){
			set_courier_prime();
		}
	}
